package com.example.mukt;

public class Feature_model {
    int image, id;

    public Feature_model(int image, int id) {
        this.image = image;
        this.id = id;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
